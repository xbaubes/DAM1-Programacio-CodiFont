// Funció per sumar dos números
function suma(a, b) {
    return a + b;
}

// Funció per restar dos números
function resta(a, b) {
    return a - b;
}

// Funció per multiplicar dos números
function multiplicacio(a, b) {
    return a * b;
}

// Funció per dividir dos números
function divisio(a, b) {
    return a / b;
}

// Exporta les funcions
module.exports = {
    suma,
    resta,
    multiplicacio,
    divisio
};
